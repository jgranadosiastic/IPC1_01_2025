    public void mostrarDialogo(int cantidadJugadores) {
        jugadores = null;
        this.cantidadJugadores = cantidadJugadores;
        
        lblNombres = new JLabel[cantidadJugadores];
        txtNombres = new JTextField[cantidadJugadores];
        for (int i = 0; i < cantidadJugadores; i++) {
            JLabel lbl = new JLabel("Nombre " + (i + 1));
            lblNombres[i] = lbl;

            JTextField txt = new JTextField();
            txtNombres[i] = txt;

            pnlJugadores.add(lbl, new org.netbeans.lib.awtextra.AbsoluteConstraints(7, 10 + (30 * i), 142, -1));
            pnlJugadores.add(txt, new org.netbeans.lib.awtextra.AbsoluteConstraints(155, 7 + (30  * i), 243, -1));

        }
        
        pack();
        this.setVisible(true);
    }

