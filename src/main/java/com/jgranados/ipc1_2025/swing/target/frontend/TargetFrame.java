    private void finalizarTurno() {
        if (partida.hayGanador()) {
            Jugador ganador = partida.obtenerGanador();
            String mensaje = String.format("Ya hay ganador, su nombre es %s y su punteo final fue de %d puntos",
                    ganador.obtenerNombre(), ganador.obtenerPunteo());
            JOptionPane.showMessageDialog(this, mensaje, "Ganador", JOptionPane.INFORMATION_MESSAGE);
            toggleBotonesTiros(false);
            lblGanador.setText("");
        } else {
            partida.cambiarJugador();
            actualizarJugadorEnTurno();
        }
    }

    private void mostrarDialogoJugadores(int cantidadJugadores) {
        pedirJugadoresDialog.mostrarDialogo(cantidadJugadores);
        if (pedirJugadoresDialog.getJugadores() != null) {
            Jugador[] jugadores = pedirJugadoresDialog.getJugadores();
            partida = new Partida(jugadores);
            toggleBotonesTiros(true);
            actualizarJugadorEnTurno();
        }
    }
    
    private void toggleBotonesTiros(boolean toggle) {
        btnTiroRapido.setEnabled(toggle);
        btnTiroBajoBrazo.setEnabled(toggle);
        btnTiroControlado.setEnabled(toggle);
    }

    public void actualizarJugadorEnTurno() {
        lblJugadorEnTurno.setText(partida.getJugadorEnTurno().obtenerNombre());
    }

    public void mostrarPuntos(int puntosObtenidos, int puntosEnTotal) {
        String mensaje = String.format("Puntos obtenidos en el turno actual: %d, Puntos obtenidos en total: %d",
                puntosObtenidos, puntosEnTotal);
        JOptionPane.showMessageDialog(this, mensaje, partida.getJugadorEnTurno().obtenerNombre(), JOptionPane.INFORMATION_MESSAGE);
    }


